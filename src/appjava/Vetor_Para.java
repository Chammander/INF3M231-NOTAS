package appjava;

public class Vetor_Para {

    public static void main(String[] args) {
        int [] vetor = {2,3,5,9,11};

        for (int i = 0; i < vetor.length; i++) {
            //System.out.printf("Valor da posição %d: %d",i, vetor[i]);
            System.out.printf("Valor da pos"+i+": "+vetor[i]+"\n");
        }
    }


}
